import { Training } from "../model/training";
import { TrainingInput } from "../types";

const trainings: Training[] = [];

const getAllTrainings = (): Training[] => { return trainings; };

const getTrainingById = (trainingId: number): Training | undefined => {
    return trainings.find(training => training.getId() === trainingId);
};

const addTraining = (training: Training): Training => {
    trainings.push(training);
    return training;
};

export default { getTrainingById, addTraining, getAllTrainings };
